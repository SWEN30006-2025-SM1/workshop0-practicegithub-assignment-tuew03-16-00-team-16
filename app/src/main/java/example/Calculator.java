package example;

public class Calculator {
    public int add(int a, int b) {
        int total = a + b;
        return total;
    }

    public int minus(int a, int b) {
        int total = a-b;
        return total;
    }

    public int multiply(int a, int b) {
        int total = a * b;
        return total;
    }

    public double divide(int a, int b) {
        int total = a/b;
        return total;
    }

    // Ray
}
